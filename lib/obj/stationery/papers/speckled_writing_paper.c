inherit PAPER_OBJ;

void setup() {
    set_short("piece of speckled writing paper");
    add_adjective("piece of speckled writing");

    set_long("With tiny speckles scattered throughout due to the different "
        "types of wood used in its construction, this sheet of writing paper "
        "has a slightly mottled texture.  Made from wood pulp that has been "
        "soaked in water, then allowed to dry out in a flat sheet, this "
        "paper could easily be covered in inked scrawls.\n");

    set_value(100);
    set_max_size(1500);

} /* setup() */
